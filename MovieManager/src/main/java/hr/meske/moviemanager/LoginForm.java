    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String guyUsername = tfUsername.getText();
        String guyPassword = jpfPassword.getText();

        if (!guyRepo.SearchTheGuyByUsername(guyUsername)) {
            MessageUtils.showErrorMessage("NotFoundError", "Seems like this Guy doesen't exist!");
            return;
        }

        if (!guyRepo.isPasswordMatch(guyUsername, guyPassword)) {
            MessageUtils.showErrorMessage("AuthorizationError", "Theese credentials don't match up!");
            return;
        }

        if (!guyRepo.isAdmin(guyUsername, guyPassword)) {
            new AdministratorForm().show();
            this.dispose();

        } else {
            try {
                new UserForm().show();
            } catch (Exception ex) {
                Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }

    }//GEN-LAST:event_btnLoginActionPerformed

