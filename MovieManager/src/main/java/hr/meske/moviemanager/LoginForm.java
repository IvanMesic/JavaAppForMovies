    private void btnRegisterLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterLoginActionPerformed
        String guyUsername = tfRegUsername.getText();
        String guyPassword = jpfPassReg.getText();

        if (guyRepo.SearchTheGuyByUsername(guyUsername)) {
            MessageUtils.showErrorMessage("RegistrationError", "This username is already taken!");
            return;
        }

         guyRepo.createTheGuy(guyUsername, guyPassword);
         
         MessageUtils.showInformationMessage("RegistrationSuccess", "New Guy Created!");
          
    }//GEN-LAST:event_btnRegisterLoginActionPerformed

